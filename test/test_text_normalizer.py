from __future__ import annotations

import importlib.util
from pathlib import Path
import sys

import pytest


MODULE_PATH = Path(__file__).resolve().parents[1] / "app" / "text_normalizer.py"
SPEC = importlib.util.spec_from_file_location("vocotype_text_normalizer", MODULE_PATH)
assert SPEC and SPEC.loader
text_normalizer = importlib.util.module_from_spec(SPEC)
sys.modules[SPEC.name] = text_normalizer
SPEC.loader.exec_module(text_normalizer)


def assert_normalized(input_text: str, expected: str) -> None:
    assert text_normalizer.normalize_chinese_numbers(input_text) == expected


def test_normalize_dates_and_basic_counts():
    assert_normalized("二零二六年四月五号我跑了三百二十米", "2026年4月5号我跑了320米")


def test_normalize_spoken_short_numbers():
    assert_normalized("一万二和一千二百三", "1.2万和1230")


def test_normalize_large_units_preserves_spoken_unit_boundaries():
    cases = {
        "恐龙大概是六千五百万年前灭绝的": "恐龙大概是6500万年前灭绝的",
        "全球人口大概八十亿人": "全球人口大概80亿人",
        "公司估值一亿二千万": "公司估值1.2亿",
        "预算是三万五": "预算是3.5万",
        "账户余额是一万二": "账户余额是1.2万",
        "项目成本二十万零三百": "项目成本200300",
    }
    for input_text, expected in cases.items():
        assert_normalized(input_text, expected)


def test_normalize_liang_in_structured_numbers():
    cases = {
        "一万两千三": "12300",
        "两千二百八": "2280",
        "一亿两千万": "1.2亿",
        "两百八十三": "283",
    }
    for input_text, expected in cases.items():
        assert_normalized(input_text, expected)


